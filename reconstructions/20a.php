<p>There is a picture of pyraminx which says over 4000 have had me. The obvious guess is that over 4000 competitions have had pyraminx and you can confirm this hypothesis on the statistics page on WCA website. </p>

<p>The next part of the question mentions that the real Pyraminx has not had it even once. The real Pyraminx refers to the real pyramids in Egypt, which is yet to have an official competition. You have to find which competition came the closest to the real pyramid. The closest competition was in Israel. </p>

<p>The competition name, venue name, city name or country name are all acceptable answers.
   
