<p> If you google 'people standing in a circle', you get references to a famous puzzle called the Josephus problem. </p>

<p>For n = 100, the last person standing will be ranked 73. 
The answer is thus the name of the 73rd ranked cuber in India for 3x3 average: Hariharan Sachidanandam. We understand that there are actually 101 people in the top 100 rankings at the time of the treasure hunt. For n=101, the answer is rank 75. Any of the 3 people sharing rank 74 will work as the answer too.

