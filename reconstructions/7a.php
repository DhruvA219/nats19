<p> The string in the question looks like some sort of ciphertext. <br>
The image in the question shows a +2.  <br>
If one googles, "ciphertext +2", one can see references to the Caesar cipher. That is one of the most basic ciphers.   <br> </p>
<p>
The offset in this case 2. So, if you add 2 to each alphabet in the question: 
<br> Z becomes B (Remember that the alphabets are considered cyclic in this case. Hence A comes after Z. So the alphabet which comes 2 after Z will be B). 
C becomes E <br>
J becomes L <br>
And so on. <br>
The final string you get is BELL PYRAMINX and that is the answer.    
